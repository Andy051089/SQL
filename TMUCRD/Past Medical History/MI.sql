SELECT ID_NO, COUNT(FEE_NO) AS VISITS_TIMES
INTO LUNGCA_FINAL_MI
FROM(
	SELECT t.ID_NO, t.FEE_NO
	FROM IPD_MI T
UNION ALL
	SELECT t1.ID_NO, t1.FEE_NO
	FROM OPD_MI T1
) AS TEMP
GROUP BY ID_NO
HAVING COUNT(FEE_NO) > 1

DROP TABLE LUNGCA_FINAL_MI

SELECT T2.FEE_NO
INTO LUNGCA_FINAL_MI
FROM (
    SELECT ID_NO
    FROM(
        SELECT t.ID_NO, t.FEE_NO
        FROM IPD_MI T
    UNION ALL
        SELECT t1.ID_NO, t1.FEE_NO
        FROM OPD_MI T1
    ) AS TEMP
    GROUP BY ID_NO
    HAVING COUNT(FEE_NO) > 1
) T1
INNER JOIN (
    SELECT ID_NO, FEE_NO
    FROM IPD_MI
    UNION ALL
    SELECT ID_NO, FEE_NO
    FROM OPD_MI
) T2 ON T1.ID_NO = T2.ID_NO