SELECT DISTINCT *
INTO OPD_MI
FROM(
	SELECT
		t1.ID_no, t.CHR_NO, t.FEE_NO,
		t.ICD10_CODE1, t.ICD10_CODE2, t.ICD10_CODE3, 
		t.ICD10_CODE4, t.ICD10_CODE5, t.ICD9_CODE1, 
		t.ICD9_CODE2, t.ICD9_CODE3, t.ICD9_CODE4, t.ICD9_CODE5
	FROM v_opd_basic_s t
	INNER JOIN v_chr_basic_s t1
	ON t.chr_no = t1.CHR_NO
union all
	SELECT
		t1.ID_no, t.CHR_NO, t.FEE_NO, 
		t.ICD10_CODE1, t.ICD10_CODE2, t.ICD10_CODE3, 
		t.ICD10_CODE4, t.ICD10_CODE5, t.ICD9_CODE1, 
		t.ICD9_CODE2, t.ICD9_CODE3, t.ICD9_CODE4, t.ICD9_CODE5
	FROM v_opd_basic_t t
	INNER JOIN v_chr_basic_T t1
	ON t.chr_no = t1.CHR_NO
union all
	SELECT
		t1.ID_no, t.CHR_NO, t.FEE_NO, 
		t.ICD10_CODE1, t.ICD10_CODE2, t.ICD10_CODE3, 
		t.ICD10_CODE4, t.ICD10_CODE5, t.ICD9_CODE1, 
		t.ICD9_CODE2, t.ICD9_CODE3, t.ICD9_CODE4, t.ICD9_CODE5
	FROM v_opd_basic_w t
	INNER JOIN v_chr_basic_W t1
	ON t.chr_no = t1.CHR_NO
) as temp
where ICD9_CODE1 like '41[02]%' or ICD9_CODE2 like '41[02]%' or ICD9_CODE3 like '41[02]%'
		or ICD9_CODE4 like '41[02]%' or ICD9_CODE5 like '41[02]%' or ICD10_CODE1 like 'I2[12]%'
		or ICD10_CODE2 like 'I2[12]%' or ICD10_CODE3 like 'I2[12]%' or ICD10_CODE4 like 'I2[12]%'
		or ICD10_CODE5 like 'I2[12]%' or ICD10_CODE1 like 'I25.2'
		or ICD10_CODE2 like 'I25.2' or ICD10_CODE3 like 'I25.2' or ICD10_CODE4 like 'I25.2'
		or ICD10_CODE5 like 'I25.2'

