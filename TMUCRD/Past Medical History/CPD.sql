SELECT DISTINCT T2.FEE_NO,T2.ID_NO
INTO LUNGCA_FINAL_CPD
FROM (
    SELECT ID_NO
    FROM(
        SELECT t.ID_NO, t.FEE_NO
        FROM IPD_CPD T
    UNION ALL
        SELECT t1.ID_NO, t1.FEE_NO
        FROM OPD_CPD T1
    ) AS TEMP
    GROUP BY ID_NO
    HAVING COUNT(FEE_NO) > 1
) T1
INNER JOIN (
    SELECT ID_NO, FEE_NO
    FROM IPD_CPD
    UNION ALL
    SELECT ID_NO, FEE_NO
    FROM OPD_CPD
) T2 ON T1.ID_NO = T2.ID_NO